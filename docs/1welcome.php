<h2>Альтернативная история - что это ?</h2>
<div class="otstyp3"></div>

<p>Альтернативная история - это новые теории реальных исторически - глобальных событий на нашей планете Земля.</p>
<p>Это история, идущая вразрез с некоторыми данными общепринятой истории. </p>
<p>Обусловлена появлением множества необъяснимых фактов, противоречивых постулатам стандартной истории. 
 К числу этих фактов относят необъяснимые  археологические находки, 
 запредельная сложность постройки уникальных  древних сооружений,
 найденные карты континентов очень старого периода.</p>
 
 
