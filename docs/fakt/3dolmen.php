<h2>Как найти дольмены?</h2>
<div class="otstyp2"></div>
<div class="car"> <img src="images/dolmen/5.jpg" ait="Дольмен" width="100%" > </div>
<div class="otstyp1"></div>
<p>Наибольшее скопление мегалитов (дольменное городище) расположено в долине Кизинки. Там их более пятисот, но, к сожалению, подавляющее большинство их разрушено и заросло. </p>
<p>Много дольменов и на побережье, где они в подавляющем большинстве случаев являются местами массового паломничества туристов и служат средством к сомнительному обогащению арендаторов клочка земли вокруг них. </p>
<p>Из известных и легкодоступных – дольмены Пшады, долины реки Жане, г. Нексис (это всё в Геленджикском районе), в пригороде Туапсе, в Аше, в Мамедовом ущелье (монолит).</p>
<p>Бесплатно посмотреть дольмены можно в Шапсугской, в Кизинке, в окрестностях пос. Нового, в отдаленных районах Туапсинского района.</p>
