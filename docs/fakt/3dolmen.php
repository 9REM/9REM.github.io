<h2>Как строили дольмены?</h2>
<div class="otstyp2"></div>
<div class="car"> <img src="images/dolmen/4.jpg" ait="Дольмен" width="100%" > </div>
<div class="otstyp1"></div>
<p>Здесь тоже много неясностей. С одной стороны – многотонные плиты из различных материалов, с другой – дольменостроители по имеющимся сведениям не знали стали… </p>
<p>Тем не менее официальная наука говорит о том что эти плиты вытесывали каким-то образом и перемещали порой на многие десятки километров, при весе иногда превышающем вес груженного Камаза. </p>
<p>Не так давно появилась версия о том, что плиты, применявшиеся при сооружении мегалитов делались на месте из специального раствора, а пазы и орнаменты легко делались на не до конца затвердевшей массе простыми орудиями.</p>

