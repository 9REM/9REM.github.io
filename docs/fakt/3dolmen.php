<h2> Что такое дольмены ?</h2>
<div class="otstyp2"></div>

<p><i>Дольмены </i> – это дословно «каменный стол» - слово пришло к нам с Британии, т.к. тамошние мегалиты как раз и представляют четыре подпорных камня, на которые несколько тысячелетий назад неизвестные взгромоздили огромную каменную плиту.</p>
<div class="otstyp1"></div>
<h2>Дольмены Краснодарского края</h2>
<div class="otstyp2"></div>
<div class="car"> <img src="images/dolmen/2.jpg" ait="Дольмен" width="100%" > </div>
<div class="otstyp1"></div>
<p>Наши же, Кавказские дольмены представляют собой совсем не стол, а скорее почти герметичную камеру, сложенную из 4 стен, опорной (пяточной) плиты и крыши. Это классическая, плиточная конструкция. Реже встречаются корытообразные дольмены – в этом случае в большом камне сверху вниз выдалбывали полость, которую потом сверху закрывали плитой. Бывают составные дольмены – когда стены составляли из нескольких плит. Наиболее редкий вид – дольмен-монолит. На Кавказе известно лишь о двух таких мегалитах. Представляют собой большой валун, через круглое отверстие в котором сооружена полость внутри.</p>
<div class="otstyp1"></div>
<h2>Зачем строили дольмены?</h2>
<div class="otstyp2"></div>
<div class="car"> <img src="images/dolmen/3.jpg" ait="Дольмен" width="100%" > </div>
<div class="otstyp1"></div>
<p>Существует множество теорий на этот счет. Официальная наука склоняется к версии, что дольмены сооружали для захоронений знати.</p>
<p>Местные казачьи легенды гласят о существовавшем некогда народе великанов, которые из жалости к их соседям лилипутам соорудили им каменные жилища. Набирает популярность версия, высказанная героиней книг Мегре Анастасией о том, что в дольмены добровольно уходили умирать древние мудрецы, для того чтобы внутри сооружения остался их дух, к которому можно обратиться за советом даже сегодня. </p>
<p>Именно поэтому каждый год тысячи людей приезжают в теплые края пообщаться, поговорить с дольменами, точнее с духами, находящимися в них. Существуют и экзотические версии: порталы в другие миры, резонаторы для увеличения плодородия почв, амбары для зерна, средства связи с инопланетянами, ульи для древних пчел… </p>
<p>Необходимо отметить, что, несмотря на то, что только в России известно несколько тысяч дольменов, мы до сих пор практически ничего не знаем о том народе, который их строил – ни культуры, ни социального устройства, ни их языка; не знаем откуда они пришли и куда делись. Скупые археологические находки крайне плохо систематизированы и описаны, а продолжающееся тысячи лет разграбление дольменов усугубляет положение.</p>

