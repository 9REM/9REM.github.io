<h2>Итак, опираясь на выше изложенную проработку ляпов,
можно сделать следующие выводы:</h2>
<div class="otstyp2"></div>
<p>Тот, кто не упустил логическую нить нашего разговора, задаст сейчас резонный вопрос: "Каким образом, тогда Луна оказывает такое мощное воздействие на биосферу Земли, если это всего лишь муляж? Ведь фазы Луны действительно влияют на множество факторов: рост волос, ногтей, рассады на грядках. Люди, связанные с магией, вообще накрепко привязаны к фазам Луны. И для чего же весь этот театр абсурда, да ещё с такими сложностями по запудриванию мозгов населению?"</p>
<p>Ответ опять в Библии:<b> для знамений </b>. Луна — это всего лишь индикатор. Как мигающий поворотник автомобиля показывает, что водитель сейчас повернёт, так и фазы Луны информируют магов на Земле, в какой фазе сейчас центральный процессор нашей планеты — Звёздная сфера по имени Олимп.</p>

